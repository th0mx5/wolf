#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (s1[len])
		len++;
	while (s2[i])
	{
		s1[i + len] = s2[i];
		i++;
	}
	s1[i + len] = '\0';
	return (s1);
}
